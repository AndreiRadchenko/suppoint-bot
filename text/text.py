def format_minutes_hhmm(total_minutes: int) -> str:
    """Convert a total-minutes value to hh:mm string (e.g. 75 → '01:15')."""
    total_minutes = max(0, int(total_minutes))
    return f"{total_minutes // 60} год. {total_minutes % 60:02d} хв."


def format_rent_status_line(rent_status: str, timer: int, locker_name: str, station_label: str) -> str | None:
    """Return a formatted status line for one rent row, or None if the status is unrecognised."""
    from math import ceil
    if rent_status == 'Очікування відкриття':
        return f"⏳ Комірка {locker_name} ({station_label}) — очікує відкриття"
    if rent_status == 'Оренда':
        if timer >= 0:
            remaining_min = max(0, int(round(timer * 15 / 60)))
            return f"📍 Комірка {locker_name} ({station_label}) — залишилось {format_minutes_hhmm(remaining_min)}"
        overtime_min = ceil(abs(timer) * 15 / 60)
        return f"⏰ Комірка {locker_name} ({station_label}) — перевищено на {format_minutes_hhmm(overtime_min)}"
    return None

